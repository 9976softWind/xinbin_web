// 手机号或座机号
export const reg_tel_phone =
  /(^((\+86)|(86))?(1[3-9])\d{9}$)|(^(0\d{2,3})-?(\d{7,8})$)/;
// 经度
export const reg_longitude =
  /^-?((1?[0-7]?[0-9]?)(([.][0-9]{1,6})?)|180(([.][0]{1,6})?))$/;
// 纬度
export const reg_latitude =
  /^-?(([1-8]?[0-9]?)(([.][0-9]{1,6})?)|90(([.][0]{1,6})?))$/;
